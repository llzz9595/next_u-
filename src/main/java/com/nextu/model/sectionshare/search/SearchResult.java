package com.nextu.model.sectionshare.search;

public class SearchResult {

	private String searchResultPart;
	private long searchResultId;
	private String searchResultType;
	private String searchResultTheme;
	private String searchResultImage;
	
	public SearchResult() {
		super();
	}
	public String getSearchResultPart() {
		return searchResultPart;
	}
	public void setSearchResultPart(String searchResultPart) {
		this.searchResultPart = searchResultPart;
	}
	public long getSearchResultId() {
		return searchResultId;
	}
	public void setSearchResultId(long searchResultId) {
		this.searchResultId = searchResultId;
	}
	public String getSearchResultType() {
		return searchResultType;
	}
	public void setSearchResultType(String searchResultType) {
		this.searchResultType = searchResultType;
	}
	public String getSearchResultTheme() {
		return searchResultTheme;
	}
	public void setSearchResultTheme(String searchResultTheme) {
		this.searchResultTheme = searchResultTheme;
	}
	public String getSerachResultImage() {
		return searchResultImage;
	}
	public void setSerachResultImage(String serachResultImage) {
		this.searchResultImage = serachResultImage;
	}
	
	
	
}
